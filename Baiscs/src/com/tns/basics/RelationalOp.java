package com.tns.basics;

public class RelationalOp {

	public static void main(String[] args) 
	{
		int a=12,b=4;
		//is equal to 
		System.out.println("a==b :"+(a==b));
		//is not equal to 
		System.out.println("a!=b :"+(a!=b));
		//is greater than
		System.out.println("a>b :"+(a>b));
		//is less than
		System.out.println("a<b :"+(a<b));
		//is greater than or equal
		System.out.println("a>=b :"+(a>=b));
		//is less than or equal
		System.out.println("a<=b :"+(a<=b));
	}

}
