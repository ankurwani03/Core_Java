package com.tns.basics;

public class LogicalOp {

	public static void main(String[] args) 
	{
		boolean a=true,b=false;
		//logical and 
		System.out.println("a && b:"+(a && b));
		//logical or
		System.out.println("a || b:"+(a || b));
		//logical not 
		System.out.println("!(a && b):"+!(a && b));
	}

}
