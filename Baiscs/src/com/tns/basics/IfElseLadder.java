package com.tns.basics;

public class IfElseLadder {

	public static void main(String[] args)
	{
		String city ="Mumbai";
		if (city=="Nashik")
		{
			System.out.println("city is Nashik");
		}
		else if (city=="pune")
		{
			System.out.println("city is pune");
		}
		else 
		{
			System.out.println(city);
		}
	}

}
